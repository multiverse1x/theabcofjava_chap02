public class Ex2_3 {
    public static void main(String[] args) {

        //계산하기
        //인텔리제이 너무좋타 >_<
        //괄호안의 계산식이 계산되어 결과가 출력됨.

        System.out.println(5+3);
        System.out.println(5-3);
        System.out.println(5*3);
        System.out.println(5/3); //정수 나누기 정수의 결과는 정수라서 5/3의 결과는 1
        System.out.println();
        System.out.println("-------------");
        System.out.println();


        // 변수에 저장해서 연산하면 훨씬 더 간편해진다.
        int x = 5; int y = 3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);



    }
}
